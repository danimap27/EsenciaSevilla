  // ================ FUNCIÓN PARA CAMBIAR IDIOMA ================
  function changeLanguage(lang) {
    // Recorremos todos los elementos que tengan data-key
    document.querySelectorAll("[data-key]").forEach((element) => {
      const key = element.getAttribute("data-key");
      if (translations[lang] && translations[lang][key]) {
        element.textContent = translations[lang][key];
      }
    });
  
    // Actualizamos el <title> del documento si tiene data-key en <head>
    const pageTitle = document.querySelector("title[data-key]");
    if (pageTitle) {
      const titleKey = pageTitle.getAttribute("data-key");
      if (translations[lang][titleKey]) {
        document.title = translations[lang][titleKey];
      }
    }
  }
  